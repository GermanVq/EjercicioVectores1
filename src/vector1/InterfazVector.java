    private void cmdMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdMostrarActionPerformed
      double suma=0,producto=1;
       double mayor=v[0];
       double menor=v[1];
        for (int i = 0; i < v.length; i++) {
            suma=suma+v[i];
            producto=producto*v[i];
            
            if(mayor<v[i]){
               mayor=v[i]; 
            }
            if(menor>v[i]){
                menor=v[i];
            }
        }
            String res1=String.valueOf(suma);
            txtSuma.setText(res1);
            String re2=String.valueOf(producto);
            txtProductoria.setText(re2);
       
            String n_mayor=String.valueOf(mayor);
            txtMayor.setText(n_mayor);
            String n_menor=String.valueOf(menor);
            txtMenor.setText(n_menor);
    }//GEN-LAST:event_cmdMostrarActionPerformed

